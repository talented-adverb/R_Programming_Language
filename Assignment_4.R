#Q4
f<-function(x){
  x*0.5*exp(-abs(x))
}
i<-integrate(f,1,10)
print(i)
f2<-function(x){
  x^2*0.5*exp(-abs(x))
}
i2<-integrate(f2,1,10)
print(i2)
var=(i2$value)-((i$value)^2)
print(paste("Mean:",i$value))
print(paste("var:",var))
print(paste("sd:",var^0.5))
